    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jFileChooser1.setLocale(Locale.getDefault());
        jFileChooser1.updateUI();
        if(jFileChooser1.showOpenDialog(this) == javax.swing.JFileChooser.APPROVE_OPTION){
            File file = jFileChooser1.getSelectedFile();
            this.jTextField1.setText(file.getAbsolutePath());
            System.out.println("greta.auxiliary.ircamalign.IrcamAlignFrame.jButton1ActionPerformed():"+file.getAbsolutePath());
            this.ircam_align.setTextfile(file.getPath());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jFileChooser2.setLocale(Locale.getDefault());
        jFileChooser2.updateUI();
        if(jFileChooser2.showOpenDialog(this) == javax.swing.JFileChooser.APPROVE_OPTION){
            File file = jFileChooser2.getSelectedFile();
            this.jTextField2.setText(file.getAbsolutePath());
            this.ircam_align.setSpeechfile(file.getPath());
        }
    }//GEN-LAST:event_jButton2ActionPerformed


